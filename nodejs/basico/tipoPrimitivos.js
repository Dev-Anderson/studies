// Tipos primitivos
// -> string: sequencia de caractesres 
// -> Number: numero inteiros ou de ponto flututante
// -> boolean: valor verdadeiro ou falso 
// -> null: valor null 
// -> undefined: valor indefinido
// -> symbol: valor unico e imutavel, introduzido no ES6

let nome = "maria"; 
let idade = 30; 
let temCarro= true; 
let nulo = null; 
let indefinido; 
let simbolo = Symbol("chave"); 

console.log("var nome: ", nome)
console.log("var idade:", idade)
console.log("var temCarro: ", temCarro)
console.log("var nulo: ", nulo)
console.log("var indefinido: ", indefinido)
console.log("var simbolo: ", simbolo)
